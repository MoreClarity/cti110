import turtle
turtle.showturtle()
def main():
    turtle.forward(100)
    turtle.write('east')
    turtle.right(180)
    turtle.forward(200)
    turtle.write('west')
    turtle.right(180)
    turtle.forward(100)
    turtle.left(90)
    turtle.forward(100)
    turtle.write('north')
    turtle.right(180)
    turtle.forward(200)
    turtle.write('south')
    turtle.right(180)
    turtle.forward(100)
    turtle.right(90)
    turtle.penup
    turtle.forward(15)
    turtle.left(90)
    turtle.pendown
    turtle.circle(15)
    turtle.hideturtle()

main()




